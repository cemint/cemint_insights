# Recommendations Display
# -------------------------------
def display_recommendations(recommendations: dict):
    """
    Display recommendations generated by AI.
    Args:
        recommendations (dict)
    """
    st.subheader("Process Recommendations")
    for stage, rec in recommendations.items():
        st.write(f"**{stage}**: {rec.get('action','No recommendation')}")
