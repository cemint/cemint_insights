import streamlit as st

# -------------------------------
# KPI Display Component
# -------------------------------
def display_kpis(kpi_dict: dict):
    """
    Display key performance indicators in dashboard.
    Args:
        kpi_dict (dict): {"SPC": 85, "TSR": 12, "Downtime": 3}
    """
    st.subheader("Key Performance Indicators (KPIs)")
    cols = st.columns(len(kpi_dict))
    for i, (key, value) in enumerate(kpi_dict.items()):
        cols[i].metric(label=key, value=value)

